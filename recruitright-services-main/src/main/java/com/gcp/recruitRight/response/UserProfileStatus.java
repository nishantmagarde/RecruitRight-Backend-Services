package com.gcp.recruitRight.response;

public class UserProfileStatus {
	
	private String isu;
	private String subIsu;
	private String projectName;
	private String jobRole;
	private String jobRoleType;
	private double profileScore;
	private String status;
	
	public String getIsu() {
		return isu;
	}
	public void setIsu(String isu) {
		this.isu = isu;
	}
	public String getSubIsu() {
		return subIsu;
	}
	public void setSubIsu(String subIsu) {
		this.subIsu = subIsu;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getJobRole() {
		return jobRole;
	}
	public void setJobRole(String jobRole) {
		this.jobRole = jobRole;
	}
	public String getJobRoleType() {
		return jobRoleType;
	}
	public void setJobRoleType(String jobRoleType) {
		this.jobRoleType = jobRoleType;
	}
	public double getProfileScore() {
		return profileScore;
	}
	public void setProfileScore(double profileScore) {
		this.profileScore = profileScore;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
