package com.gcp.recruitRight.models;

public class Requirement {

		private int reqId;
		private String userId;
		private String isu;
		private String subIsu;
		private String projectName;
		private String jobRole;
		private String jobRoleType;
		private String techStack;
		private double experience;
		private String status;

		public int getReqId() {
			return reqId;
		}
		public void setReqId(int reqId) {
			this.reqId = reqId;
		}
		public String getUserId() {
			return userId;
		}
		public void setUserId(String userId) {
			this.userId = userId;
		}
		public String getIsu() {
			return isu;
		}
		public void setIsu(String isu) {
			this.isu = isu;
		}
		public String getSubIsu() {
			return subIsu;
		}
		public void setSubIsu(String subIsu) {
			this.subIsu = subIsu;
		}
		public String getProjectName() {
			return projectName;
		}
		public void setProjectName(String projectName) {
			this.projectName = projectName;
		}
		public String getJobRole() {
			return jobRole;
		}
		public void setJobRole(String jobRole) {
			this.jobRole = jobRole;
		}
		public String getJobRoleType() {
			return jobRoleType;
		}
		public void setJobRoleType(String jobRoleType) {
			this.jobRoleType = jobRoleType;
		}
		public String getTechStack() {
			return techStack;
		}
		public void setTechStack(String techStack) {
			this.techStack = techStack;
		}
		public double getExperience() {
			return experience;
		}
		public void setExperience(double experience) {
			this.experience = experience;
		}
		public String getStatus() {
			return status;
		}
		public void setStatus(String status) {
			this.status = status;
		}

		
		

	}

